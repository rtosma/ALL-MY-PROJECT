int	ft_str_is_numeric(char *str)
{
	int	k;

	k = 0;
	while (str[k] != '\0')
	{
		if (!(str[k] >= 48 && str[k] <= 57))
		{
			return (0);
		}
		k++;
	}
	return (1);
}
