int	ft_str_is_alpha(char *str)
{
	int	k;

	k = 0;
	while (str[k] == '\0')
	{
		return (1);
	}
	while (str[k] != '\0')
	{
		if ((str[k] >= 65 && str[k] <= 90) || (str[k] >= 97 && str[k] <= 122))
		{
			k++;
		}
		else
		{
			return (0);
		}
	}
	return (1);
}
